package com.Rays;

public class Sum {
public static void main(String[] args) {
	 int a = 8;
	 int b = 9;
	 int c = 6;
	 int f = a*b+c;
	 int g = a+b*c;
	 int h = a-b-c;
	 System.out.println(a+b*c);
	 System.out.println(a*b*c);
	 System.out.println(a-c*b);
	 System.out.println(h*g+f-a*b+f);  //etc. 
}
}
